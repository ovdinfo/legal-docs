function saveInMemory() {
  let current = getData()
  let appeal26Data = current ? JSON.stringify(current) : {}
  window.localStorage.setItem('appeal26Data', appeal26Data)
}

function clearMemory() {
  window.localStorage.setItem('appeal26Data', JSON.stringify({}))
}

function loadFromMemory() {
  let appeal26Data = window.localStorage.getItem('appeal26Data')
  let loaded = appeal26Data ? JSON.parse(appeal26Data) : {}
  for (var key in loaded) {
    if (loaded.hasOwnProperty(key)) {
      let value = loaded[key]
      if(value) {
        let el = document.getElementById(key)
        if(el) {
          el.value = value
        } else {
          if(value.constructor === Array) {
            for (var i = 0; i < value.length; i++) {
              document.querySelector('[value="' + value[i] + '"]').checked = 'checked'
            }
          } else {
            document.querySelector('[value="' + value + '"]').checked = 'checked'
          }
        }
      }
    }
  }
}

